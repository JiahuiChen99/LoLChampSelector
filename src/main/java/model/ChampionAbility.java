package model;


public class ChampionAbility {

    private String champion_name;
    private String passive;
    private String q;
    private String w;
    private String e;
    private String r;

    public String getChampionName() {
        return this.champion_name;
    }

    public String getPassive() {
        return passive;
    }

    public String getQ() {
        return q;
    }

    public String getW() {
        return w;
    }

    public String getE() {
        return e;
    }

    public String getR() {
        return r;
    }
}
